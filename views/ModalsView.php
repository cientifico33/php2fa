      <div class="modal-body">
        
        <p>Desde su tienda de aplicaciones (PlayStore o AppStore según su dispositivo) descargue "Twilio Authy Authenticator", posteriormente abra la aplicación para iniciar el proceso.</p>

        <p>Seleccione "Guatemala" e ingrese el número de su teléfono celular.</p>
        <img src="cons/images/instrucciones/Authy_1.png" class="shadow-lg">

        <p>Presione "Enviar" y confirme que su número está escrito correctamente.</p>
        <img src="cons/images/instrucciones/Authy_3.png" class="shadow-lg">

        <p>En la siguiente vista se solicita confirmar su número, seleccione la opción "Recibir mensaje SMS" e ingrese el código recibido en su teléfono.</p>
        <img src="cons/images/instrucciones/Authy_4.png" class="shadow-lg">

        <p>Presione el botón + para iniciar el proceso de vinculación.</p>
        <img src="cons/images/instrucciones/Authy_5.png" class="shadow-lg">

        <p>Toque el botón "Escanear código QR".</p>
        <img src="cons/images/instrucciones/Authy_6.png" class="shadow-lg">

        <p>En caso se le solicite acceso a la cámara, presione "Permitir".</p>
        <img src="cons/images/instrucciones/Authy_7.png" class="shadow-lg">

        <p>Toque la opción "Logo genérico" y presione "Continuar".</p>
        <img src="cons/images/instrucciones/Authy_8.png" class="shadow-lg">

        <p>Finalmente presione "Guardar".</p>
        <img src="cons/images/instrucciones/Authy_9.png" class="shadow-lg">

        <p>Podrá ver el código que debe ingresar en la página y termine el proceso precionando el botón "Vincular".</p>
        <img src="cons/images/instrucciones/Vincular.jpg" class="shadow-lg">

      </div>
